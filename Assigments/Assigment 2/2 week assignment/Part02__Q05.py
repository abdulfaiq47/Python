"""Part-2: Loops
Q:05 Write a program to calculate the sum of all numbers from 1 to 50 using for loop.

"""


total=0# start sum to 0


for i in range(1, 51):# Use a for loop to start from 1 to 50
    total +=i  # Add each number to the total


print("The sum of numbers from 1 to 50 is:", total)
